package com.baido.QLBD.Model;

public class xevao {
	private String idXe;
	private String idThe;
	private String idBD;
	private String bsx;
	private String loai;
	private String ngayVao;
	public xevao() {
		super();
	}
	public xevao(String idXe, String idThe, String idBD, String bsx, String loai, String ngayVao) {
		super();
		this.idXe = idXe;
		this.idThe = idThe;
		this.idBD = idBD;
		this.bsx = bsx;
		this.loai = loai;
		this.ngayVao = ngayVao;
	}
	public String getIdXe() {
		return idXe;
	}
	public void setIdXe(String idXe) {
		this.idXe = idXe;
	}
	public String getIdThe() {
		return idThe;
	}
	public void setIdThe(String idThe) {
		this.idThe = idThe;
	}
	public String getIdBD() {
		return idBD;
	}
	public void setIdBD(String idBD) {
		this.idBD = idBD;
	}
	public String getBsx() {
		return bsx;
	}
	public void setBsx(String bsx) {
		this.bsx = bsx;
	}
	public String getLoai() {
		return loai;
	}
	public void setLoai(String loai) {
		this.loai = loai;
	}
	public String getNgayVao() {
		return ngayVao;
	}
	public void setNgayVao(String ngayVao) {
		this.ngayVao = ngayVao;
	}
	
	

}
